import { createServerClient, type CookieOptions } from '@supabase/ssr';
import { NextResponse, type NextRequest } from 'next/server';

export async function middleware(request: NextRequest) {
  let response = NextResponse.next({
    request: {
      headers: request.headers,
    },
  });

  const supabase = createServerClient(
    process.env.NEXT_PUBLIC_SUPABASE_URL!,
    process.env.NEXT_PUBLIC_SUPABASE_ANON_KEY!,
    {
      cookies: {
        get(name: string) {
          return request.cookies.get(name)?.value;
        },
        set(name: string, value: string, options: CookieOptions) {
          request.cookies.set({
            name,
            value,
            ...options,
          });
          response = NextResponse.next({
            request: {
              headers: request.headers,
            },
          });
          response.cookies.set({
            name,
            value,
            ...options,
          });
        },
        remove(name: string, options: CookieOptions) {
          request.cookies.set({
            name,
            value: '',
            ...options,
          });
          response = NextResponse.next({
            request: {
              headers: request.headers,
            },
          });
          response.cookies.set({
            name,
            value: '',
            ...options,
          });
        },
      },
    }
  );

  const {
    data: { user },
  } = await supabase.auth.getUser();

  // Public routes (unauthenticated access allowed)
  const pathname = request.nextUrl.pathname;
  const isAuthPath =
    pathname.startsWith('/auth/login') || pathname.startsWith('/auth/signup');
  const isPublicPath =
    pathname === '/' || pathname.startsWith('/learn');
  const isApiRoute = pathname.startsWith('/api');

  // Allow all API routes to handle their own auth
  if (isApiRoute) {
    return response;
  }

  // Require auth for non-public paths (e.g., /admin, /chat)
  if (!user && !isAuthPath && !isPublicPath) {
    return NextResponse.redirect(new URL('/auth/login', request.url));
  }

  // If signed in, prevent visiting auth pages
  if (user && isAuthPath) {
    return NextResponse.redirect(new URL('/learn', request.url));
  }

  return response;
}

export const config = {
  matcher: [
    /*
     * Match all request paths except for the ones starting with:
     * - _next/static (static files)
     * - _next/image (image optimization files)
     * - favicon.ico (favicon file)
     */
    '/((?!_next/static|_next/image|favicon.ico).*)',
  ],
};